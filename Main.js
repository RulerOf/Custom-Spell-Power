var DEBUG = false;

var g_Mult = 5.0;
var g_Cap = 80.0;
var g_Spells = true, g_Items = true, g_Heroes = true, g_SpecText = "Spells & Items & Heroes";



var replacedMap = [];
function onGetAbilityValue(ability, abilityName, field, values)
{
	var fullName = abilityName + "." + field;
	
	if (typeof replacedMap[fullName] != "undefined") return;
	replacedMap[fullName] = true;
	
	if (abilityName.indexOf("item_") != -1)
	{
		if (!g_Items)
			return;
	}
	else
	{
		if (!g_Spells)
			return;
	}
	
	for (var i in capedParam)
	{
		if (fullName == capedParam[i])
		{
			if (DEBUG) printToAll("capedParam : " + fullName);
			return changePower(values, true, true);
		}
	}
	
	for (var i in decreaseParam)
	{
		if (fullName == decreaseParam[i])
		{
			if (DEBUG) printToAll("decreaseParam : " + fullName);
			return changePower(values, false, false);
		}
	}
	
	for (var i in increaseParam)
	{
		if (fullName == increaseParam[i])
		{
			if (DEBUG) printToAll("increaseParam : " + fullName);
			return changePower(values, true, false);
		}
	}
	
	if (DEBUG) printToAll("justSkipped : " + fullName + " : " + values);
	
	return;
}

function onUnitParsed(unit, keyvalues)
{
	var unitClassName = unit.getClassname();
	var modelPath = "type: string " + keyvalues["Model"];
	
	if (g_Spells)
	{
		for (var i in spellSummoned)
		{
			if (modelPath.indexOf(spellSummoned[i]) != -1)
			{
				//game.precacheModel(keyvalues["Model"], true);
				//keyvalues["ModelScale"] += (0.10 * g_Mult);
				keyvalues["ArmorPhysical"] *= g_Mult;
				keyvalues["AttackDamageMin"] *= g_Mult;
				keyvalues["AttackDamageMax"] *= g_Mult;
				keyvalues["AttackRate"] -= (0.20 * g_Mult);
				keyvalues["MovementSpeed"] += (20 * g_Mult);
				
				keyvalues["StatusHealth"] *= g_Mult;
				keyvalues["StatusHealthRegen"] *= g_Mult;
				keyvalues["StatusMana"] *= g_Mult;
				keyvalues["StatusManaRegen"] *= g_Mult;
				
				break;
			}
		}
	}
	
	if (g_Items)
	{
		for (var i in itemSummoned)
		{
			if (modelPath.indexOf(spellSummoned[i]) != -1)
			{
				//keyvalues["ModelScale"] += (0.10 * g_Mult);
				keyvalues["ArmorPhysical"] *= g_Mult;
				keyvalues["AttackDamageMin"] *= g_Mult;
				keyvalues["AttackDamageMax"] *= g_Mult;
				keyvalues["AttackRate"] -= (0.20 * g_Mult);
				keyvalues["MovementSpeed"] += (20 * g_Mult);
				
				keyvalues["StatusHealth"] *= g_Mult;
				keyvalues["StatusHealthRegen"] *= g_Mult;
				keyvalues["StatusMana"] *= g_Mult;
				keyvalues["StatusManaRegen"] *= g_Mult;
				
				break;
			}
		}
	}
	
	if (g_Heroes)
	{
		if (dota.heroes.indexOf(unitClassName) != -1)
		{
			keyvalues["AttributeBaseStrength"] *= g_Mult;
			keyvalues["AttributeBaseAgility"] *= g_Mult;
			keyvalues["AttributeBaseIntelligence"] *= g_Mult;
			
			keyvalues["AttributeStrengthGain"] *= g_Mult;
			keyvalues["AttributeAgilityGain"] *= g_Mult;
			keyvalues["AttributeIntelligenceGain"] *= g_Mult;
		}
	}
}

function changePower(values, isInc, isCap)
{
	var oldValues = values;
	var cap = g_Cap;
	
	function aglMult(v)
	{
		var cur_chance;
		var chance = Math.sqrt(v * v);
		cap = Math.max(chance, cap);
		chance /= 100;

		cur_chance = chance;

		for (var i = 0; i < (g_Mult / 1) - 1; i++)
			cur_chance = (cur_chance + (1 - cur_chance) * chance);
			
		cur_chance *= 100;

		if (v < 0) cur_chance * -1;

		return Math.round(cur_chance);
	}
	
	if (isInc)
	{
		if (isCap)
		{

			//values = values.map(aglMult); // crush???
			values = values.map(function(v) {return v * g_Mult;});
			
			values = values.map(function(v) {return Math.min(v, cap);});
			values = values.map(function(v) {return Math.max(v, -cap);});
		}
		else
			values = values.map(function(v) {return v * g_Mult;});
	}
	else
	{
		values = values.map(function(v) {return v / g_Mult;});
	}
	
	if (DEBUG) printToAll("Power changed: " + oldValues + " => " + values);
	
	return values;
}

function onEntityHurt(event)
{
	var attacked = game.getEntityByIndex(event.getInt("entindex_killed"));
	var attacker = game.getEntityByIndex(event.getInt("entindex_attacker"));
	var inflictor = game.getEntityByIndex(event.getInt("entindex_inflictor"));
	var damageType = event.getInt("damagebits");
	
	var inflictorName = inflictor.getClassname();
	
	if (DEBUG) printToAll("onEntityHurt");
	
	for (var i in nonBoostDamageSpells)
	{
		if (nonBoostDamageSpells[i][0].indexOf(inflictorName) != -1)
		{
			var skillLevel = inflictor.netprops.m_iLevel;
			var dmg = nonBoostDamageSpells[i][skillLevel] * (g_Mult - 1);
			
			dota.applyDamage(attacker, attacked, inflictor, dmg, damageType);
			
			if (DEBUG) printToAll("Added extra damage: " + dmg);
		}
	}
}




